class ApartmentSale:
    url: str
    startDate: str
    endDate: str

    def setUrl(self, url):
        self.url = url

    def getUrl(self):
        return self.url

    def setStartDate(self, startDate):
        self.startDate = startDate

    def getStartDate(self):
        return self.startDate

    def setEndDate(self, endDate):
        self.endDate = endDate
    
    def getEndDate(self):
        return self.endDate
   